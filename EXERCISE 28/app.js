"use strict";
//     AGE DETERMINDER
//  creating a veriable
let age = 22;
if (age < 2) {
    console.log("\n You are a Baby.");
}
else if (age >= 2 && age < 4) {
    console.log("\n You are a Toddler.");
}
else if (age >= 4 && age < 13) {
    console.log("\n You are a Kid.");
}
else if (age >= 13 && age < 20) {
    console.log("\n You are a Teenager.");
}
else if (age >= 20 && age < 65) {
    console.log("\n You are an Adult.");
}
else if (age >= 65) {
    console.log("\n You are an Elder.");
}
